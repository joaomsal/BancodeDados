package Beans;

public class Titulo {
    private String Titulo, ISBN, anoPub, Editora_obra, autor;

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getISBN() {
        return ISBN;
    }

    public void setISBN(String ISBN) {
        this.ISBN = ISBN;
    }

    public String getAnoPub() {
        return anoPub;
    }

    public void setAnoPub(String anoPub) {
        this.anoPub = anoPub;
    }

    public String getEditora_obra() {
        return Editora_obra;
    }

    public void setEditora_obra(String Editora_obra) {
        this.Editora_obra = Editora_obra;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }
}
